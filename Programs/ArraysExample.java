public class ArraysExample{
	public static void main(String args[]){
		int arr[] = new int[5]; //Instantiation
		int sec[] = {2, 3, 6, 12, 67, 23}; //Direct initialization
		//index      0  1  2   3   4    5
		
		arr[0] = 12;
		arr[1] = 23;
		arr[2] = 56;
		arr[3] = 67;
		arr[4] = 13; 
		System.out.println("Element 1 -> arr[0] :- "+ arr[4]) ; 
		System.out.println("Element 2 -> sec[3] :- "+ sec[4]) ; 
		 
	}
}